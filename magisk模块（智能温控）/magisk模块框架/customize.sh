#!/system/bin/sh
# ============================================================
# 飞智 B6X 增强计划 — 模块安装脚本
# ============================================================
# 刷入时 Magisk 自动执行，设置文件权限
# ============================================================

# 设置二进制可执行权限
set_perm "$MODPATH/tempctrl" 0 0 0755

# 设置配置文件权限（可读可写，保护隐私）
set_perm "$MODPATH/profile.conf" 0 0 0644

# 设置 service.sh 可执行
set_perm "$MODPATH/service.sh" 0 0 0755
